package me.nelonn.customitems.mixin;

import net.minecraft.world.InteractionHand;
import net.minecraft.world.InteractionResultHolder;
import net.minecraft.world.entity.player.Player;
import net.minecraft.world.item.ItemStack;
import net.minecraft.world.item.KnowledgeBookItem;
import net.minecraft.world.level.Level;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfoReturnable;

@Mixin(KnowledgeBookItem.class)
public class KnowledgeBookItemMixin {

    @Inject(method = "use", at = @At(value = "INVOKE", target = "Lorg/slf4j/Logger;error(Ljava/lang/String;Ljava/lang/Object;)V", remap = false), cancellable = true)
    private void inject_use(Level world, Player user, InteractionHand hand, CallbackInfoReturnable<InteractionResultHolder<ItemStack>> cir) {
        // I want to skip these lines:
        // LOGGER.error("Invalid recipe: {}", (Object) string);
        // LOGGER.error("Tag not valid: {}", (Object) compoundTag);
        cir.setReturnValue(InteractionResultHolder.fail(user.getItemInHand(hand)));
    }

}
